class CharacterValues
{
  readonly Space = " ";
  readonly Tab = "\t";
  readonly CarriageReturn = "\r";
  readonly NewLine = "\n";
  readonly LessThan = "<";
  readonly Equals = "=";
  readonly DoubleQuote = "\"";
  readonly Slash = "/";
  readonly GreaterThan = ">";
}

export const Characters = new CharacterValues();